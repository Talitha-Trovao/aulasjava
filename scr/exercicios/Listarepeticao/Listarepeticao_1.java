package Listarepeticao;

public class Listarepeticao_1 {
    public static void main(String[] args) {
        int i;
        i = 1;
        while(i<=20){
            System.out.println("Eu gosto de estudar algoritmos!");
            i = i+1;
        }
        
    }
    
}